function simple_alert(type,head,message){
  toastr.options = alert_options();
  if (type == 6) {
    toastr.error(message,head);
  } else if (type == 2) {
    toastr.success(message,head);
  } else if (type == 3) {
    toastr.info(message,head);
  } else if (type == 4) {
    toastr.warning(message,head);
  } else if (type == 5) {
    toastr.error(message,head);
  }else {
    toastr.error('wrong input type !', 'Error!');
  }
}

function form_wizard(){
  $('.wizard_control a[data-toggle="tab"]').on('shown.bs.tab', function (e) {
    $('.wizard_control a[data-toggle="tab"]').removeClass('btn-primary');
    $('.wizard_control a[data-toggle="tab"]').addClass('btn-default');
    $(this).removeClass('btn-default');
    $(this).addClass('btn-primary');
  });
  $('.next').click(function () {
    var nextId = $(this).parents('.tab-pane').next().attr("id");
    $('.wizard_control [href=#'+nextId+']').tab('show');
  });
  $('.prev').click(function () {
    var prevId = $(this).parents('.tab-pane').prev().attr("id");
    $('.wizard_control [href=#'+prevId+']').tab('show');
  });
  $('.wizard_control .btn-wizard').addClass('disabled');
}

function show_wizard(tab_num,is_active){
  $('a[href="#step'+tab_num+'"]').tab('show');
  $('a[data-toggle="tab"]').removeClass('btn-primary');
  $('a[data-toggle="tab"]').addClass('btn-default');
  $('a[href="#step'+tab_num+'"]').removeClass('btn-default');
  $('a[href="#step'+tab_num+'"]').addClass('btn-primary');
  if (is_active) {
    $('.btn-wizard').removeClass('disabled');
  }
}

function check_session_status() {
  $.ajax({
      async: true,
      type: 'POST',
      dataType: 'json',
      url: '<?php echo site_url("Login/session_status"); ?>',
      success: function (data) {
        if (data['status'] == 0) {
            location.reload();
        }
      }
  });
}

